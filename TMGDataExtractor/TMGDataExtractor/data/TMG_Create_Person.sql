CREATE TABLE person (
    "PER_NO" INTEGER PRIMARY KEY NOT NULL,
    "FATHER" INTEGER,
    "MOTHER" INTEGER,
    "LAST_EDIT" DATETIME NOT NULL,
    "DSID" INTEGER NOT NULL,
    "REF_ID" INTEGER,
    "REFERENCE" VARCHAR(12),
    "SPOULAST" INTEGER,
    "SCBUFF" VARCHAR(10),
    "PBIRTH" VARCHAR(30),
    "PDEATH" VARCHAR(30),
    "SEX" CHAR(1) NOT NULL,
    "LIVING" CHAR(1) NOT NULL,
    "BIRTHORDER" CHAR(2) NOT NULL,
    "MULTIBIRTH" CHAR(1) NOT NULL,
    "ADOPTED" CHAR(1) NOT NULL,
    "ANCE_INT" CHAR(1) NOT NULL,
    "DESC_INT" CHAR(1) NOT NULL,
    "RELATE" INTEGER,
    "RELATEFO" INTEGER,
    "TT" CHAR(1),
    "FLAG1" CHAR(1)
);
CREATE UNIQUE INDEX "ix_per_no" on person (PER_NO ASC);
CREATE INDEX "ix_father" on person (FATHER ASC);
CREATE INDEX "ix_mother" on person (MOTHER ASC);
